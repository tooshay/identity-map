<?php

namespace AppBundle\XmlImporter\IdentityMap;

abstract class AbstractIdentity implements IdentityInterface
{
    private $id;

    /**
     * Identity constructor.
     * @param $id
     */
    public function __construct($id)
    {
        $this->id = $id;
    }

    /**
     * @return mixed
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * @param mixed $id
     */
    public function setId($id)
    {
        $this->id = $id;
    }
}
